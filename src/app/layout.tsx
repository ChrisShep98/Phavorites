import type { Metadata } from "next";
import { AR_One_Sans } from "next/font/google";
import { AuthProvder } from "./providers/SessionProvider";
import "./globals.css";
import { getServerSession } from "next-auth";
import { ThemeProvider } from "./providers/ThemeProvider";
import { SongContextProvider } from "./providers/SongContextProvider";
import { ModalContextProvider } from "./providers/ModalContextProvider";
import Nav from "./components/Nav";

const arOneSans = AR_One_Sans({
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "Phish",
  description: "Generated by create next app",
  icons: {
    icon: [
      {
        url: "/images/Red_circle.png",
      },
    ],
  },
};

export default async function RootLayout({ children }: { children: React.ReactNode }) {
  const session = await getServerSession();
  return (
    <html lang="en">
      <body className={arOneSans.className} style={{ scrollbarGutter: "stable" }}>
        <ThemeProvider>
          <SongContextProvider>
            <ModalContextProvider>
              <AuthProvder session={session}>
                <Nav>{children}</Nav>
              </AuthProvder>
            </ModalContextProvider>
          </SongContextProvider>
        </ThemeProvider>
      </body>
    </html>
  );
}
